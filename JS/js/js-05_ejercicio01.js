/*
ejercicio 1 un programas que solicite al usuario dos números y en consola nos va a decir si esos números son iguales o ay alguno que sea mayor que otro
*/
var numD1 = prompt("Inserta un numero");
let numD2 = parseInt(prompt("Ingrese un segundo numero"));
if (numD1==numD2){
    alert("HOLA,  tu numeros son iguales");
}else if(numD1>numD2){
    alert("HOLA, tu primer numero es mayor");
}else{
    alert("HOLA, tu segundo numero es el mayor");
}

